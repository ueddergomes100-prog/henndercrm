function sale(
  id: number,
  soldAt: string,
  clientId: number | undefined,
  sellerId: number,
  totalValue: number,
  discountValue = 0,
): UniplusSale {
  const client = mockUniplusClients.find((item) => item.id === clientId);
  return {
    id,
    soldAt,
    includedAt: `${soldAt}T10:00:00-03:00`,
    changedAt: `${soldAt}T10:05:00-03:00`,
    clientId,
    clientName: client?.name,
    clientDocument: client?.document,
    sellerId,
    totalValue,
    discountValue,
    status: "FATURADO",
    approved: true,
  };
}

